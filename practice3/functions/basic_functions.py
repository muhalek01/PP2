# basic_functions.py
# 4 basic function examples

# Example 1
def greet(name):
    return f"Hello, {name}!"

print(greet("Alice"))

# Example 2
def add(a, b):
    return a + b

print(add(5, 3))

# Example 3
def is_even(number):
    return number % 2 == 0

print(is_even(10))

# Example 4
def get_length(text):
    return len(text)

print(get_length("Python"))
